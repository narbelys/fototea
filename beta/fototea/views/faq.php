<dl class="faq">
    <ol>
        <li>
            <dt id="1_1">¿Qué es Fototea?</dt>
            <dd>Fototea es una plataforma de contratación de servicios audiovisuales que permite a clientes de diverso tipo contratar a fotógrafos y productores de video.
                Somos un mercado online que facilita que clientes descubran, comparen y contraten a proveedores de servicio para sus necesidades de producción o edición de fotografía y video.
            </dd>
        </li>
        <li>
            <dt id="1_2">¿Cómo funciona Fototea?</dt>
            <dd>El ingreso y registro a la plataforma es gratuito tanto para clientes como para Fotógrafos y Videografos. Una vez registrados, los clientes suben sus necesidades a la plataforma y los creativos interesados envían, de forma privada, sus ofertas comerciales.
                El cliente evalúa las propuestas recibidas y elige a cual Fotógrafo o Videografo adjudicar el servicio requerido. El pago se realiza a través de la plataforma y es retenido por Fototea hasta que el servicio se ha realizado de forma satisfactoria.
                Finalmente, el pago es enviado al proveedor de servicio.
            </dd>
        </li>
        <li>
            <dt id="1_3">¿Cuáles son los beneficios de Fototea?</dt>
            <dd>Fototea otorga a los clientes:
                <ul>
                    <li>Acceso rápido y fácil a un grupo único de talentos creativos</li>
                    <li>Herramientas para fácil comparación del talento</li>
                    <li>La devolución de su dinero en caso de que el proveedor de servicio no cumpla con lo contratado</li>
                </ul>
                Fototea otorga a los proveedores de servicio:
                <ul>
                    <li>Acceso a fuentes adicionales de ingreso</li>
                    <li>Iniciar relaciones profesionales duraderas con nuevos clientes</li>
                    <li>Cobro seguro y garantizado por los servicios prestados a sus clientes</li>
                </ul>
            </dd>
        </li>
    </ol>
</dl>

