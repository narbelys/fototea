        <i class="glyphicon glyphicon-chevron-right"></i>Clientes
    </div>

<dl class="faq">
    <ol>
        <li>
            <dt id="2_1">¿Cómo encuentro profesionales para mis proyectos?</dt>
            <dd>Una vez te has registrado como cliente, tendrás la posibilidad de subir tus requerimientos a la plataforma. Estos requerimientos serán mostrados a los proveedores de servicio registrados en Fototea quienes, dependiendo de su disponibilidad, experiencia e intereses pueden hacerte de manera privada una oferta comercial para tu evaluación.
            </dd>
        </li>
        <li>
            <dt id="2_2">¿Cómo saber qué profesional es adecuado para mi proyecto?</dt>
            <dd>Existen múltiples variables que puedes evaluar y comparar en Fototea. Además del precio propuesto por el servicio, tendrás acceso a información adicional del proveedor de servicio como portafolio, opiniones de clientes anteriores, experiencia previa, equipo disponible, etc.
                Puedes además contactar a los proveedores de servicio a través de las herramientas de comunicación interna en caso de que tengas alguna duda adicional.
            </dd>
        </li>
        <li>
            <dt id="2_3">¿Qué trabajos puedo externalizar a través de Fototea?</dt>
            <dd>En Fototea puedes externalizar necesidades de producción o edición de fotografía y video. Así, puedes encontrar a Fotógrafos y Videografos para eventos, productos, lugares y cualquier tipo de ocasión.
            </dd>
        </li>
        <li>
            <dt id="2_4">¿Qué tipo de profesionales puedo encontrar en Fototea?</dt>
            <dd>En Fototea podrás encontrar a proveedores de servicios de producción y edición de fotografía y video.
            </dd>
        </li>
        <li>
            <dt id="2_5">¿Cómo puedo supervisar el avance de los proyectos?</dt>
            <dd>Una vez hayas adjudicado tu proyecto a un proveedor de servicios, tendrás acceso a toda su información de contacto, mediante la cual podrás establecer el método de comunicación de tu preferencia.
                De igual manera, la plataforma de mensajería interna podrá seguir siendo usada como método de contacto.
            </dd>
        </li>
        <li>
            <dt id="2_6">¿Cuánto cuesta usar Fototea?</dt>
            <dd>Fototea es de gratis acceso tanto para clientes como para proveedores de servicio. Fototea únicamente cobra una comisión estándar sobre el precio acordado entre cliente y proveedor de servicio en caso de que una transacción se complete a través de la plataforma.
            </dd>
        </li>
        <li>
            <dt id="2_7">¿Cómo les pago a los profesionales que contrato?</dt>
            <dd>El pago se realiza directamente en la plataforma a través de nuestro punto de pago. Puedes pagar utilizando tarjetas de crédito, débito, transferencia bancaria o Paypal.
            </dd>
        </li>
        <li>
            <dt id="2_8">¿Qué pasa si no estoy conforme con el trabajo realizado?</dt>
            <dd>En caso de que no estés conforme con el trabajo realizado, debes seguir las instrucciones del módulo de opiniones una vez culminado el servicio. En este caso, se disparará el mecanismo de resolución de disputas en el que un fotógrafo profesional externo decidirá si el trabajo entregado incumplió con estándares de calidad y profesionalismo.
            </dd>
        </li>
    </ol>
</dl>

